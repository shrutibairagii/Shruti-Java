class CentimeterToMeter
{
	public static void main(String[] args)
	{
		float cm1 = 0.01f;
		float cm2 = 0.02f;
		float cm3 = 0.03f;
		float cm4 = 0.04f;
		float cm5 = 0.05f;

		System.out.println("1 Centimeter Convert into Meter -"+cm1 +"m");
		System.out.println("2 Centimeter Convert into Meter -"+cm2 +"m");
		System.out.println("3 Centimeter Convert into Meter -"+cm3 +"m");
		System.out.println("4 Centimeter Convert into Meter -"+cm4 +"m");
		System.out.println("5 Centimeter Convert into Meter -"+cm5 +"m");
	}
}