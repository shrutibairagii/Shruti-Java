class SquareRootDouble
{
	public static void main(String[] args)
	{
		double squareRoot10 = 3.1622776601684;
		double squareRoot20 = 4.4721359549996;
		double squareRoot30 = 5.4772255750517;
		double squareRoot40 = 6.3245553203368;
		double squareRoot50 = 7.0710678118655;

		System.out.println("Square Root of 10 is - "+squareRoot10);
		System.out.println("Square Root of 20 is - "+squareRoot20);
		System.out.println("Square Root of 30 is - "+squareRoot30);
		System.out.println("Square Root of 40 is - "+squareRoot40);
		System.out.println("Square Root of 50 is - "+squareRoot50);
	}
}