class RsConvertFloat 
{
	public static void main(String[] args)
	{
		float indianToUsd = 0.012f;
		float canadianDollar = 0.01637f;
		float japaneseYen = 1.80813f;
		float koreanWon = 16.091f;
		float singaporeDollar = 0.01622f;

		System.out.println("1 Indian Rupee Convert into United States Dollar - "+indianToUsd);
		System.out.println("1 Indian Rupee Convert into Canadian Dollar - "+canadianDollar);
		System.out.println("1 Indian Rupee Convert into Japanese Yen - "+japaneseYen);
		System.out.println("1 Indian Rupee Convert into Korean Won - "+koreanWon);
		System.out.println("1 Indian Rupee Convert into Singapore Dollar - "+singaporeDollar);
	}
}